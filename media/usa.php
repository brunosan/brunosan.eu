					<h3>Why the US Can Beat China: The Facts About SpaceX Costs</h3>
					<div class="hr3"><hr /></div>
					
					<p><strong class="blue smallText">May 4, 2011</strong></p>
					<br />
					
					<p>Whenever someone proposes to do something that has never been done before, there will always be skeptics.</p>  
					
					<p>So when I started SpaceX, it was not surprising when people said we wouldn&#8217;t succeed. But now that we&#8217;ve successfully proven Falcon 1, Falcon 9 and Dragon, there&#8217;s been a steady stream of misinformation and doubt expressed about SpaceX&#8217;s actual launch costs and prices.</p>
 
					<p><a href="http://www.aviationweek.com/aw/generic/story.jsp?id=news/asd/2011/04/15/11.xml&channel=space" target="_blank">As noted last month by a Chinese government official</a>, SpaceX currently has the best launch prices in the world and they don&#8217;t believe they can beat them. This is a clear case of American innovation trumping lower overseas labor rates.</p>  
					
					<p>I recognize that our prices shatter the historical cost models of government-led developments, but these prices are not arbitrary, premised on capturing a dominant share of the market, or &#8220;teaser&#8221; rates meant to lure in an eager market only to be increased later. These prices are based on known costs and a demonstrated track record, and they exemplify the potential of America's commercial space industry.</p>
 
					<p>Here are the facts:</p>
 
					<p><strong>The price of a standard flight on a Falcon 9 rocket is $54 million.</strong> We are the only launch company that publicly posts this information on our website (<a href="http://www.spacex.com/" target="_blank">www.spacex.com</a>). We have signed many legally binding contracts with both government and commercial customers for this price (or less).  Because SpaceX is so vertically integrated, we know and can control the overwhelming majority of our costs.  This is why I am so confident that our performance will increase and our prices will decline over time, as is the case  with every other technology.</p>
 
					<p><strong>The average price of a full-up NASA Dragon cargo mission to the International Space Station is $133 million including inflation</strong>, or roughly $115m in today&#8217;s dollars, and we have a firm, fixed price contract with NASA for 12 missions. This price includes the costs of the Falcon 9 launch, the Dragon spacecraft, all operations, maintenance and overhead, and all of the work required to integrate with the Space Station. If there are cost overruns, SpaceX will cover the difference.  (This concept may be foreign to some traditional government space contractors that seem to believe that cost overruns should be the responsibility of the taxpayer.)</p> 
 
					<p><strong>The total company expenditures since being founded in 2002 through the 2010 fiscal year were less than $800 million</strong>, which includes all the development costs for the Falcon 1, Falcon 9 and Dragon. Included in this $800 million are the costs of building launch sites at Vandenberg, Cape Canaveral and Kwajalein, as well as the corporate manufacturing facility that can support up to 12 Falcon 9 and Dragon missions per year. This total also includes the cost of five flights of Falcon 1, two flights of Falcon 9, and one up and back flight of Dragon.</p>
 
					<p><strong>The Falcon 9 launch vehicle was developed from a blank sheet to first launch in four and half years for just over $300 million.</strong> The Falcon 9 is an EELV class vehicle that generates roughly one million pounds of thrust (four times the maximum thrust of a Boeing 747) and carries more payload to orbit than a Delta IV Medium.</p>
 
					<p><strong>The Dragon spacecraft was developed from a blank sheet to the first demonstration flight in just over four years for about $300 million.</strong> Last year, SpaceX became the first private company, in partnership with NASA, to successfully orbit and recover a spacecraft. The spacecraft and the Falcon 9 rocket that carried it were designed, manufactured and launched by American workers for an American company. The Falcon 9/Dragon system, with the addition of a launch escape system, seats and upgraded life support, can carry seven astronauts to orbit, more than double the capacity of the Russian Soyuz, but at less than a third of the price per seat.</p>
 
					<p><strong>SpaceX has been profitable every year since 2007</strong>, despite dramatic employee growth and major infrastructure and operations investments. We have over 40 flights on manifest representing over $3 billion in revenues.</p>
 
					<p>These are the objective facts, confirmed by external auditors. Moreover, SpaceX intends to make far more dramatic reductions in price in the long term when full launch vehicle reusability is achieved. We will not be satisfied with our progress until we have achieved this long sought goal of the space industry.</p>
 
					<p>For the first time in more than three decades, America last year began taking back international market-share in commercial satellite launch. This remarkable turn-around was sparked by a small investment NASA made in SpaceX in 2006 as part of the Commercial Orbital Transportation Services (COTS) program.  A unique public-private partnership, COTS has proven that under the right conditions, a properly incentivized contractor &#8212; even an all-American one &#8212; can develop extremely complex systems on rapid timelines and a fixed-price basis, significantly beating historical industry-standard costs.</p>
 
					<p>China has the fastest growing economy in the world. But the American free enterprise system, which allows anyone with a better mouse-trap to compete, is what will ensure that the United States remains the world&#8217;s greatest superpower of innovation.</p>
					<p>--Elon--</p>

