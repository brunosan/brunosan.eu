	<p class='pheight30'>
	In 2009 we celebrate 400 years of Galileo's observations, the background theme for 
	the International Year of Astronomy 2009. 
	</p><p class='pheight30'>
	One of Galileo's study objects was our star - the Sun, where he observed one of the 
	first know types of solar activity - Sunspots. 400 years have past since Galileo's observations, 
	but there are still a large number of people around the world that think the Sun is just a 
	bright perfect yellow ball. 
	</p><p>
	And this is where you come in!
	</p><p class='pheight30'>
	<em><b>Dawn of the IYA2009</b></em> is a global, Sun observation campaign, proposed by <a href='http://www.astro.up.pt'>Centro de Astrofísica da Universidade do Porto</a> (CAUP) 
and the IYA2009's <a href='http://www.astronomy2009.org/organisation/structure/taskgroups/solarphysics/'>Solar Physics Group</a> (SPG), which seeks not only to be the global teaser for the <a href='http://www.astronomy2009.org/'>IYA2009</a> (a first calling card), but also to show to a larger, global public, what our star is all about. 
	</p><p class='pheight30'>
	For this activity we propose that, starting at local noon, 1st of January 2009, everyone with means to (safely) observe the Sun, gather outside science centers, town halls, shopping centers or simply your own street, and present the Sun and the IYA2009 to the world. 
	</p><p class='pheight30'>
	We call all professional and hobby astronomers to participate in what will be the largest Sun observation event in the world. Whether you have a telescope with a solar filter, an H-alpha telescope, a sunspotter or a simple pinhole projection system, you are the one we need.
	</p><p class='pheight30'>
	All registered participants will download a set of instructions on how to safely observe the Sun, a IYA2009 logo to place on their equipment and/or a small poster presenting the IYA2009, which should be present during the activity.
	</p><p class='pheight30'>
	Since the 1st of January 2009 is in the winter for the northern hemisphere, some alternative indoor sites should be considered. These alternative sites could show multimedia material and exhibits about the Sun (for example, the SOHO webpage), where some kind of reference to the IYA2009 must be present. Keeping with the spirit of the activity, these places can be a small tent in your street, to local cultural or sports pavilions with a computer connected to the Internet, to Planetariums and science centers presenting multimedia shows about the Sun.
	</p>

