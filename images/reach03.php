<p class="header">Taurus, beware of UFOs!


<p class="author"><a href="https://www.brunosan.eu">Dr Bruno S�nchez-Andrade Nu�o</a><br>College of Science, George Mason University, VA



<p>In general, the most important part in any outreach activity I conduct is to
engage the audience into looking at the world, at the universe... at
everything with curiosity. Leave them always with more questions than they
had before; make them want to know more, make then think about what they
hear... make them realize what universe they are living in.

<p>Going down to some particularities in outreach is this frequent and worrying
concept I found now and then during these years: Astrology and UFO-logy
being widely confused with astronomy. This is something I believe everyone
should pay attention to. You should talk about life on other planets (and
what "real" about this topic) but warn them about phony UFO claims. When you
talk about stars and constellations, separate it completely from any
astrological link.

<p>I remember one time that I had to present an informal talk. I did not have
any telescopes, blackboard or PowerPoint presentation... and I had to engage
a group of adults about the marvels of the universe, only with my voice. The
group was participating on an international event and they were already
quite tired. I could have used the usual "blah-blah" of numbers and
distances... and three minutes later everyone would be sleeping. What I did
was to take advantage of the setting we were at (a lovely sunny afternoon on
the Slovenian
countryside) and I pushed the group to guide the talk by engaging them
through questions and getting them to help work out the answers. It took a
while for everyone to get in the mood, get over their shyness and fear they
would say something wrong, but they soon started responding. We started with
some balls and steps to represent the scale of the solar system. In the end
it was a complete success and the session continued well into the starry
night.

<p>As an extra hint. People are mostly interested in a handful of topics, so it
is not difficult to prepare some answers or tricks along these
lines: Black holes, constellations, what is a star, how big is the universe,
...
















